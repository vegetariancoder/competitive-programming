nums = [3,4,5]

if nums[0] + nums[1] <= nums[2]:
    print("none")
elif nums[0] == nums[1] == nums[2]:
    print("equilateral")
elif nums[0] == nums[1] or nums[1] == nums[2]:
    print("isosceles")
else:
    print("scalene")

